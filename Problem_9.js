function remainder (num_1, num_2){
    return num_1 % num_2;
}

let Remainder_1 = remainder(1, 3);
console.log(Remainder_1);

let Remainder_2 = remainder(3, 4);
console.log(Remainder_2);

let Remainder_3 = remainder(-9, 45);
console.log(Remainder_3);

let Remainder_4 = remainder(5, 5);
console.log(Remainder_4);
